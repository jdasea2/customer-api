package com.example.domain;

public class CustomerInfo{
	String name;
	String id;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getName() {
		return this.name;
	}
	
    public void setName(String name) {
    	this.name = name;
    }
}